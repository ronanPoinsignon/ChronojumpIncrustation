package prog;

public class WindowImage {

	private String coureur, cheval;
	private int penalite;
	private float chrono;
	
	public WindowImage() {
		
	}

	public String getCoureur() {
		return coureur;
	}

	public void setCoureur(String coureur) {
		this.coureur = coureur;
	}

	public String getCheval() {
		return cheval;
	}

	public void setCheval(String cheval) {
		this.cheval = cheval;
	}

	public int getPenalite() {
		return penalite;
	}

	public void setPenalite(int penalite) {
		this.penalite = penalite;
	}

	public float getChrono() {
		return chrono;
	}

	public void setChrono(float chrono) {
		this.chrono = chrono;
	}
	
}
